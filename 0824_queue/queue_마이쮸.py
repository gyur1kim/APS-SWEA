p = 1 # 처음 줄 설 사람 번호

q = []
N = 20   # 초기 마이쮸 개수
m = 0    # 나눠준 개수

while m < N:
    q.append((p, 1, 0)) # 처음 줄 선 사람의 번호, 몇 개 받아갔니?
    v, c, my = q.pop(0)   # v는 사람 번호, c는 줄 선 횟수, my는 내가 여지껏 받은 수
    m += c   # 나눠준 사탕 수 갱신
    q.append((v, c+1, my+c))  # 사람 번호, 줄 선 횟수, 내가 받아간 사탕 수
    p += 1   # 처음 줄 서는 사람의 번호 증가

